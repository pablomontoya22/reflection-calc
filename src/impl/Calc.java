package impl;

public class Calc {

	public double suma(double n1, double n2) {
		return n1 + n2;
	}

	public double resta(double n1, double n2) {
		return n1 - n2;
	}

	public double multi(double n1, double n2) {
		return n1 * n2;
	}

	public double div(double n1, double n2) {
		return n1 / n2;
	}
}
